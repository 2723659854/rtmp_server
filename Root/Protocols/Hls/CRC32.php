    /**
     * crc32校验
     * @param $data
     * @return string
     */
    public function calculateCRC32($data) {
        $crc32 = 0xFFFFFFFF;

        for ($i = 0; $i < strlen($data); $i++) {
            $crc32 = ($crc32 >> 8) ^ $this->crc32Table[($crc32 ^ ord($data[$i])) & 0xFF];
        }

        return dechex($crc32);
    }
